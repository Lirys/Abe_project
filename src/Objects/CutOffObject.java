package Objects;

public class CutOffObject {
    
    private int cutoffID;
    private int employeeID;
    private int no_of_days;
    private int no_of_holidays;

    public void setCutoffID(int cutoffID) {
        this.cutoffID = cutoffID;
    }

    public int getCutoffID() {
        return cutoffID;
    }

    public void setEmployeeID(int employeeID) {
        this.employeeID = employeeID;
    }

    public int getEmployeeID() {
        return employeeID;
    }

    public void setNo_of_days(int no_of_days) {
        this.no_of_days = no_of_days;
    }

    public int getNo_of_days() {
        return no_of_days;
    }

    public void setNo_of_holidays(int no_of_holidays) {
        this.no_of_holidays = no_of_holidays;
    }
    public int getNo_of_holidays() {
        return no_of_holidays;
    }
}